public class Toppings{
    private Boolean Chess=true;
    private Boolean Peperoni;
    private Boolean Pineapple;
    private Boolean Chicken;
    private Boolean Mushroom;
    public Toppings(Boolean Peperoni, Boolean Pineapple, Boolean Chicken, Boolean Mushroom) {
        this.Peperoni = Peperoni;
        this.Pineapple = Pineapple;
        this.Chicken = Chicken;
        this.Mushroom = Mushroom;
    }
    public void setChess(Boolean Chess) {
        this.Chess = Chess;
    }

    public void setPeperoni(Boolean Peperoni) {
        this.Peperoni = Peperoni;
    }

    public void setPineapple(Boolean Pineapple) {
        this.Pineapple = Pineapple;
    }

    public void setChicken(Boolean Chicken) {
        this.Chicken = Chicken;
    }

    public void setMushroom(Boolean Mushroom) {
        this.Mushroom = Mushroom;
    }


    
}
